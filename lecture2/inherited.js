//simple inheritance with number
//method .toFixed;

var num = 5.56789;
var n = num.toFixed(2);
console.log(n);


//method .toPrecision;
var num = 5.56789;
var n = num.toPrecision(3);
console.log(n);

//method .toString;
var num = 5.56789;
var n = num.toString();
console.log(n);

//method boolean.toString;
var num = true;
var n = num.toString();
console.log(n);
console.log(typeof n);